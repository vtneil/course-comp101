s = 'Earth'

# 1. Get a character at index
# with string[index]
print(s[0])
print(s[2])
print(s[-1])
print(s[-2])
print('-----')

# 2. Get a substring from at start
# index to before stop index
# with string[start:stop]
print(s[1:3])
print(s[-4:-1])
print(s[2:])
print(s[:-2])
print(s[:])
print('-----')

# 3. Get a substring from start to stop
# every step characters
# with string[start:stop:step]
print(s[0:5:2])
print(s[::])
print(s[::-1])
print('-----')
